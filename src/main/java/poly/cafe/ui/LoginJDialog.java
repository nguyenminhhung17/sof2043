     @Override
    public void login() {
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword()); // dùng getPassword an toàn hơn
        UserDAO dao = new UserDAOImpl();
        User user = dao.findByUsername(username);

        if (user == null) {
            XDialog.alert("Sai tên đăng nhập!");
        } else if (!password.equals(user.getPassword())) {
            XDialog.alert("Sai mật khẩu đăng nhập!");
        } else if (!user.isEnabled()) {
            XDialog.alert("Tài khoản của bạn đang tạm dừng!");
        } else {
            XAuth.user = user;          // duy trì user đăng nhập
            loginSuccessful = true;     // <-- đánh dấu đăng nhập thành công
            this.dispose();             // đóng dialog
        }
    }

